// api/remove-background.js
export default async function handler(req, res) {
  if (req.method !== 'POST') {
    return res.status(405).json({ error: 'Method not allowed' });
  }
 
  try {
    const { imageBase64, format } = req.body;
    if (!imageBase64) return res.status(400).json({ error: 'No image provided' });
 
    const base64Data = imageBase64.split(',')[1] || imageBase64;
    const imageBuffer = Buffer.from(base64Data, 'base64');
 
    const boundary = '----FormBoundary' + Math.random().toString(36).slice(2);
 
    const textPart = Buffer.from(
      `--${boundary}\r\nContent-Disposition: form-data; name="size"\r\n\r\nauto\r\n` +
      `--${boundary}\r\nContent-Disposition: form-data; name="format"\r\n\r\n${format || 'png'}\r\n` +
      `--${boundary}\r\nContent-Disposition: form-data; name="image_file"; filename="image.jpg"\r\nContent-Type: image/jpeg\r\n\r\n`
    );
    const endPart = Buffer.from(`\r\n--${boundary}--\r\n`);
    const body = Buffer.concat([textPart, imageBuffer, endPart]);
 
    const response = await fetch('https://api.remove.bg/v1.0/removebg', {
      method: 'POST',
      headers: {
        'X-Api-Key': process.env.REMOVEBG_API_KEY,
        'Content-Type': `multipart/form-data; boundary=${boundary}`,
      },
      body: body,
    });
 
    if (!response.ok) {
      const errorText = await response.text();
      return res.status(response.status).json({ error: errorText });
    }
 
    const resultBuffer = await response.arrayBuffer();
    const resultBase64 = Buffer.from(resultBuffer).toString('base64');
    return res.status(200).json({ image: `data:image/png;base64,${resultBase64}` });
 
  } catch (err) {
    return res.status(500).json({ error: err.message });
  }
}
 
